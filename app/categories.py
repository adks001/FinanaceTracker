class DuplicateCategoryError(Exception):
    """Raised when attempting to add a category that already exists globally or for the user."""
    pass


def add_custom_category(supabase_client, user_id: str, category_name: str, svg_icon: str = None) -> dict:
    """
    Checks for a duplicate category (case-insensitive) under global categories
    or specific user categories. If duplicate found, raises DuplicateCategoryError.
    Otherwise, inserts the new custom category into Supabase categories table.
    """
    clean_name = category_name.strip()
    if not clean_name:
        raise ValueError("Category name cannot be empty.")
    
    # 1. Query supabase for any matching category (either global OR for this specific user)
    # We query using lowercase comparison:
    response = supabase_client.table("categories").select("*").or_(
        f"profile_id.is.null,profile_id.eq.{user_id}"
    ).execute()
    
    existing_categories = response.data or []
    
    # Check for case-insensitive duplicate in local array
    for cat in existing_categories:
        if cat["name"].lower() == clean_name.lower():
            raise DuplicateCategoryError(
                f"Category '{clean_name}' already exists (as a global or custom category)."
            )
            
    # 2. Insert if no duplicate exists
    icon_to_use = svg_icon if svg_icon and svg_icon.strip() else DEFAULT_CUSTOM_SVG
    
    insert_data = {
        "profile_id": user_id,
        "name": clean_name,
        "svg_icon": icon_to_use
    }
    
    insert_response = supabase_client.table("categories").insert(insert_data).execute()
    if not insert_response.data:
        raise RuntimeError("Failed to insert custom category into database.")
        
    return insert_response.data[0]
